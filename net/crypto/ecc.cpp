EllipticCurve::EllipticCurve(const BigNum& p_, const BigNum& a_, const BigNum& b_,
                              const BigNum& order_, const ECPoint& gen_)
    : p(p_), a(a_), b(b_), order(order_), generator(gen_) {}

ECPoint EllipticCurve::point_neg(const ECPoint& point) const {
    if (point.is_infinity) return point;
    return {point.x, BigNum(0u).mod_sub(point.y, p), false};
}

bool EllipticCurve::is_on_curve(const ECPoint& point) const {
    if (point.is_infinity) return true;
    BigNum lhs = point.y.mod_mul(point.y, p);
    BigNum x3 = point.x.mod_mul(point.x, p).mod_mul(point.x, p);
    BigNum ax = a.mod_mul(point.x, p);
    BigNum rhs = x3.mod_add(ax, p).mod_add(b, p);
    return lhs.compare(rhs) == 0;
}

ECPoint EllipticCurve::double_point(const ECPoint& pt) const {
    if (pt.is_infinity || pt.y.is_zero()) {
        return {BigNum(0u), BigNum(0u), true};
    }
    BigNum three(3u);
    BigNum two(2u);
    BigNum xsq = pt.x.mod_mul(pt.x, p);
    BigNum num = three.mod_mul(xsq, p).mod_add(a, p);
    BigNum den = two.mod_mul(pt.y, p);
    BigNum s = num.mod_mul(den.mod_inverse(p), p);
    BigNum x3 = s.mod_mul(s, p).mod_sub(pt.x.mod_add(pt.x, p), p);
    BigNum y3 = s.mod_mul(pt.x.mod_sub(x3, p), p).mod_sub(pt.y, p);
    return {x3, y3, false};
}

ECPoint EllipticCurve::point_add(const ECPoint& p1, const ECPoint& p2) const {
    if (p1.is_infinity) return p2;
    if (p2.is_infinity) return p1;
    if (p1.x.compare(p2.x) == 0) {
        if (p1.y.compare(p2.y) != 0) {
            return {BigNum(0u), BigNum(0u), true};
        }
        return double_point(p1);
    }
    BigNum dy = p2.y.mod_sub(p1.y, p);
    BigNum dx = p2.x.mod_sub(p1.x, p);
    BigNum s = dy.mod_mul(dx.mod_inverse(p), p);
    BigNum x3 = s.mod_mul(s, p).mod_sub(p1.x, p).mod_sub(p2.x, p);
    BigNum y3 = s.mod_mul(p1.x.mod_sub(x3, p), p).mod_sub(p1.y, p);
    return {x3, y3, false};
}

ECPoint EllipticCurve::point_mul(const ECPoint& point, const BigNum& scalar) const {
    ECPoint result = {BigNum(0u), BigNum(0u), true};
    ECPoint base = point;
    std::size_t bits = scalar.bit_length();
    for (std::size_t i = 0; i < bits; i++) {
        if ((scalar.limbs()[i / 32] >> (i % 32)) & 1) {
            result = point_add(result, base);
        }
        base = double_point(base);
    }
    return result;
}

