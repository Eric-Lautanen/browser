// ---------------------------------------------------------------------------
// Icon atlas — Option A (dedicated bitmap icons)
//
// 10 icons × 16 px wide = 160 px → packed into a 256×16 GL_R8 texture.
// Each icon occupies a 16×16 cell; the atlas row is a single 256-wide strip.
//
// Atlas layout (x offset, icon):
//   0   BACK
//   16  FORWARD
//   32  REFRESH
//   48  STOP
//   64  CLOSE
//   80  MINIMIZE
//   96  MAXIMIZE
//   112 MENU
//   128 BOOKMARK_ON
//   144 BOOKMARK_OFF
//
// Bitmaps are 1-bit (0 / 255) stored as u8.  They were drawn on a 16×16 grid
// with (0,0) at top-left, readable as ASCII art below each definition.
// ---------------------------------------------------------------------------

