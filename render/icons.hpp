// ---------------------------------------------------------------------------
// IconAtlas — owns the packed GL_R8 texture and exposes draw helpers
// ---------------------------------------------------------------------------

class IconAtlas {
public:
    // Build the 256×16 atlas from the static bitmaps above.
    // Call once after the GL context is ready (e.g. after Renderer::initialize).
    Result<void> initialize() {
        // Pack all bitmaps into one 256-wide × 16-tall row
        u8 buf[ATLAS_W * ATLAS_H] = {};

        const u8* srcs[ICON_COUNT] = {
            ICON_BACK, ICON_FORWARD, ICON_REFRESH, ICON_STOP, ICON_CLOSE,
            ICON_MINIMIZE, ICON_MAXIMIZE, ICON_MENU,
            ICON_BOOKMARK_ON, ICON_BOOKMARK_OFF
        };

        for (u32 i = 0; i < ICON_COUNT; ++i) {
            u32 ox = i * ICON_W;
            for (u32 row = 0; row < ICON_H; ++row) {
                for (u32 col = 0; col < ICON_W; ++col) {
                    buf[row * ATLAS_W + ox + col] = srcs[i][row * ICON_W + col];
                }
            }
        }

        return texture_.create(ATLAS_W, ATLAS_H, buf);
    }

    // Draw one icon.
    //  x, y  — top-left of the destination rectangle (screen pixels)
    //  size  — desired render size (icons scale; 16 is native 1:1)
    //  color — tint / alpha; use Color::WHITE for no tint
    void draw(Renderer* r, Icon icon, f32 x, f32 y, f32 size, const Color& color) const {
        u32 idx = static_cast<u32>(icon);
        if (idx >= ICON_COUNT) return;

        // UV extents of this icon's cell in the 256-wide atlas
        f32 u0 = (idx * ICON_W)           / static_cast<f32>(ATLAS_W);
        f32 u1 = (idx * ICON_W + ICON_W)  / static_cast<f32>(ATLAS_W);
        constexpr f32 v0 = 0.0f;
        constexpr f32 v1 = 1.0f;

        r->begin_textured(const_cast<Texture2D*>(&texture_));
        r->add_tex_quad(x, y, size, size, color, u0, v0, u1, v1);
        r->end_textured();
    }

    // Convenience: center an icon inside a button rectangle
    void draw_centered(Renderer* r, Icon icon,
                       f32 btn_x, f32 btn_y, f32 btn_w, f32 btn_h,
                       f32 icon_size, const Color& color) const {
        f32 ix = btn_x + (btn_w - icon_size) * 0.5f;
        f32 iy = btn_y + (btn_h - icon_size) * 0.5f;
        draw(r, icon, ix, iy, icon_size, color);
    }

    Texture2D& texture() { return texture_; }

private:
    Texture2D texture_;
};

